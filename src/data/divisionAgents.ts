export interface AgentDef {
  id: string;
  name: string;
  subtitle: string;
  icon: string;
  status: 'active' | 'running' | 'paused';
  statusLabel: string;
  accent: 'blue' | 'green' | 'amber' | 'purple';
  division: string;
  divisionName: string;
  metrics: { label: string; value: string }[];
  description: string;
  category: 'operations' | 'intelligence' | 'safety' | 'logistics';
  deepDiveLink?: string;
  /** Number of running instances of this agent type */
  instances: number;
  /** Tasks completed today */
  tasksToday: number;
  /** Tasks completed this week */
  tasksThisWeek: number;
  /** Uptime percentage (e.g. 99.95) */
  uptimePercent: number;
}

/** Activity log entry returned by the agent detail API */
export interface AgentActivity {
  timestamp: string;
  action: string;
  detail: string;
}

