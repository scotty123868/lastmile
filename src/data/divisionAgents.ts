/** Northbridge family division IDs */
const northbridgeFamily = ['northbridge', 'nb-aerospace', 'nb-energy', 'nb-financial', 'nb-health'];

/** Estonia family division IDs */
const estoniaFamily = ['estonia', 'ee-finance', 'ee-social', 'ee-economic', 'ee-ria'];

/** Returns division-specific agents + shared platform agents */
export function getAgentsForDivision(divisionId: string): AgentDef[] {
  if (divisionId === 'meridian' || divisionId === 'shared') {
    return allAgents;
  }
  // Parent divisions include their sub-divisions
  if (divisionId === 'northbridge') {
    return allAgents.filter(
      (a) => northbridgeFamily.includes(a.division) || a.division === 'shared',
    );
  }
  if (divisionId === 'estonia') {
    return allAgents.filter(
      (a) => estoniaFamily.includes(a.division) || a.division === 'shared',
    );
  }
  return allAgents.filter(
    (a) => a.division === divisionId || a.division === 'shared',
  );
}

